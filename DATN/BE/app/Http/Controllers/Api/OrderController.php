    public function index(Request $request)
    {
        $user = Auth::user();

        // ‚úÖ Check role: N·∫øu Admin l·∫•y h·∫øt, User ch·ªâ l·∫•y c·ªßa m√¨nh
        if ($user->role === 'Admin') {
            $orders = Order::with(['user', 'orderItems.productVariant'])
