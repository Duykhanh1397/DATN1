    /**
     * üìå [Guest] Check ƒë∆°n h√†ng qua SƒêT ho·∫∑c m√£ ƒë∆°n
     * - FE g·ªçi: POST /api/orders/guest-check
     * - Body: { "phone_number": "0987xxx", "order_code": "ABC123XYZ" }
     */
    public function guestCheckOrder(Request $request)
    {
        $request->validate([
            'phone_number' => 'required_without:order_code|string',
            'order_code'   => 'required_without:phone_number|string'
        ]);

        // ‚úÖ T√¨m theo sƒët ho·∫∑c m√£ ƒë∆°n
        $query = Order::with(['orderItems.productVariant', 'voucher']);

        if ($request->filled('phone_number')) {
            $query->where('guest_user', $request->phone_number);
        }

        if ($request->filled('order_code')) {
            $query->where('order_code', $request->order_code);
        }

        $order = $query->first();

        if (!$order) {
            return response()->json(['status' => false, 'message' => 'Kh√¥ng t√¨m th·∫•y ƒë∆°n h√†ng'], 404);
        }

        return response()->json([
            'status' => true,
            'message' => 'Th√¥ng tin ƒë∆°n h√†ng',
            'data' => $order
        ]);
    }

    /**
     * üìå [Admin] C·∫≠p nh·∫≠t tr·∫°ng th√°i ƒë∆°n h√†ng
     * - FE Admin g·ªçi: PUT /api/admin/orders/{orderId}/update-status
     * - Body: { "status": "ƒê√£ x√°c nh·∫≠n" }
     */
    public function updateStatus(Request $request, $orderId)
    {
        $request->validate([
            'status' => 'required|string|in:Ch·ªù x√°c nh·∫≠n,ƒê√£ x√°c nh·∫≠n,ƒêang giao h√†ng,Giao h√†ng th√†nh c√¥ng,Giao h√†ng th·∫•t b·∫°i,H·ªßy ƒë∆°n'
        ]);

        $order = Order::findOrFail($orderId);
        $user = Auth::user();

        // ‚úÖ C·∫≠p nh·∫≠t tr·∫°ng th√°i
        $order->update(['status' => $request->status]);

        // ‚úÖ Ghi log l·ªãch s·ª≠ tr·∫°ng th√°i
        OrderStatusHistory::create([
            'order_id' => $order->id,
            'user_id' => $user->id,
            'status' => $request->status
        ]);

        return response()->json([
            'status' => true,
            'message' => 'C·∫≠p nh·∫≠t tr·∫°ng th√°i ƒë∆°n h√†ng th√†nh c√¥ng'
        ]);
    }

    /**
     * üìå [User ho·∫∑c Guest] ƒê·∫∑t h√†ng
     * - FE g·ªçi: POST /api/orders
     * - Body:
     *   {
     *     "user_id": optional,
     *     "phone_number": "b·∫Øt bu·ªôc",
     *     "address": "b·∫Øt bu·ªôc",
     *     "order_items": [
     *          { "product_variant_id": 1, "quantity": 2 }
     *      ],
     *     "voucher_code": optional
     *   }
     */
    public function create(Request $request)
    {
        try {
            $user = Auth::user();

            $rules = [
                'order_items' => 'required|array',
                'order_items.*.product_variant_id' => 'required|exists:product_variants,id',
                'order_items.*.quantity' => 'required|integer|min:1',
                'voucher_code' => 'nullable|string',
                'payment_method' => 'required|string|in:COD,VNPay',
                'shipping_fee' => 'nullable|numeric|min:0',
                'address' => 'required|string'
            ];

            if (!$user) {
                $rules['phone_number'] = 'required|string';
            }

            $validated = $request->validate($rules);

            $phone = $user ? $user->phone : $request->phone_number;
            $address = $request->address;

            if (!$phone || !$address) {
                return response()->json([
                    'status' => false,
                    'message' => 'Thi·∫øu th√¥ng tin s·ªë ƒëi·ªán tho·∫°i ho·∫∑c ƒë·ªãa ch·ªâ'
                ], 400);
            }

            // L·∫•y th√¥ng tin voucher n·∫øu c√≥ v√† ki·ªÉm tra t√≠nh h·ª£p l·ªá
            $voucher = null;
            $discount = 0;
            if ($request->voucher_code) {
                $voucher = Voucher::where('code', $request->voucher_code)
                    ->where('status', 'Ho·∫°t ƒë·ªông')
                    ->first();

                if (!$voucher) {
                    return response()->json([
                        'status' => false,
                        'message' => 'Voucher kh√¥ng h·ª£p l·ªá ho·∫∑c ƒë√£ h·∫øt h·∫°n'
                    ], 400);
                }

                // Ki·ªÉm tra xem user ƒë√£ s·ª≠ d·ª•ng voucher n√†y ch∆∞a
                if ($user) {
                    $hasUsed = VoucherUser::where('user_id', $user->id)
                        ->where('voucher_id', $voucher->id)
                        ->exists();
                    if ($hasUsed) {
                        return response()->json([
                            'status' => false,
                            'message' => 'B·∫°n ƒë√£ s·ª≠ d·ª•ng voucher n√†y r·ªìi. M·ªói t√†i kho·∫£n ch·ªâ ƒë∆∞·ª£c s·ª≠ d·ª•ng voucher m·ªôt l·∫ßn.'
                        ], 400);
                    }
                }

                // Ki·ªÉm tra c√°c ƒëi·ªÅu ki·ªán kh√°c c·ªßa voucher
                $now = Carbon::now();
                if ($voucher->used_count >= $voucher->usage_limit) {
                    return response()->json([
                        'status' => false,
                        'message' => "Voucher ƒë√£ h·∫øt l∆∞·ª£t s·ª≠ d·ª•ng (ƒë√£ d√πng: {$voucher->used_count}/{$voucher->usage_limit})"
                    ], 400);
                }

                if ($voucher->start_date && Carbon::parse($voucher->start_date)->gt($now)) {
                    $startDateLocal = Carbon::parse($voucher->start_date)->setTimezone('Asia/Ho_Chi_Minh');
                    return response()->json([
                        'status' => false,
                        'message' => "Voucher ch∆∞a c√≥ hi·ªáu l·ª±c (b·∫Øt ƒë·∫ßu t·ª´: {$startDateLocal->format('Y-m-d H:i:s')})"
                    ], 400);
                }

                if ($voucher->end_date && Carbon::parse($voucher->end_date)->lt($now)) {
                    $endDateLocal = Carbon::parse($voucher->end_date)->setTimezone('Asia/Ho_Chi_Minh');
                    return response()->json([
                        'status' => false,
                        'message' => "Voucher ƒë√£ h·∫øt h·∫°n (k·∫øt th√∫c v√†o: {$endDateLocal->format('Y-m-d H:i:s')})"
                    ], 400);
                }
            }

            // T√≠nh t·ªïng gi√° s·∫£n ph·∫©m
            $subtotal = 0;
            foreach ($request->order_items as $item) {
                $variant = ProductVariant::find($item['product_variant_id']);
                if (!$variant) {
                    return response()->json([
                        'status' => false,
                        'message' => 'Kh√¥ng t√¨m th·∫•y s·∫£n ph·∫©m v·ªõi ID: ' . $item['product_variant_id']
                    ], 400);
                }

                $subtotal += $variant->price * $item['quantity'];
            }

            // Ki·ªÉm tra gi√° tr·ªã ƒë∆°n h√†ng t·ªëi thi·ªÉu n·∫øu c√≥ voucher
            if ($voucher && $voucher->min_order_value && $subtotal < $voucher->min_order_value) {
                return response()->json([
                    'status' => false,
                    'message' => "Gi√° tr·ªã ƒë∆°n h√†ng ch∆∞a ƒë·∫°t m·ª©c t·ªëi thi·ªÉu ƒë·ªÉ √°p d·ª•ng voucher (t·ªëi thi·ªÉu: " . number_format($voucher->min_order_value, 0, ',', '.') . " VNƒê)"
                ], 400);
            }

            // T√≠nh gi·∫£m gi√° t·ª´ voucher
            if ($voucher) {
                if ($voucher->discount_type === 'percentage') {
                    $discount = $subtotal * ($voucher->discount_value / 100);
                    if ($voucher->max_discount !== null) {
                        $discount = min($discount, (float) $voucher->max_discount);
                    }
                    Log::info('Discount calculated for percentage voucher', [
                        'voucher_id' => $voucher->id,
                        'subtotal' => $subtotal,
                        'discount_value' => $voucher->discount_value,
                        'max_discount' => $voucher->max_discount,
                        'discount' => $discount,
                    ]);
                } else {
                    $discount = $voucher->discount_value;
                    Log::info('Discount calculated for fixed voucher', [
                        'voucher_id' => $voucher->id,
                        'subtotal' => $subtotal,
                        'discount_value' => $voucher->discount_value,
                        'discount' => $discount,
                    ]);
                }
            }

            $shippingFee = $request->shipping_fee ?? 0;
            $finalTotal = max($subtotal - $discount + $shippingFee, 0);

            Log::info('Order totals calculated', [
                'subtotal' => $subtotal,
                'discount' => $discount,
                'shipping_fee' => $shippingFee,
                'final_total' => $finalTotal,
            ]);

            // T·∫°o ƒë∆°n h√†ng v·ªõi ƒë·ªãa ch·ªâ v·ª´a nh·∫≠p
            $order = Order::create([
                'user_id' => $user ? $user->id : null,
                'guest_user' => $user ? null : $phone,
                'status' => 'Ch·ªù x√°c nh·∫≠n',
                'total_amount' => $finalTotal,
                'phone_number' => $phone,
                'address' => $address,
                'order_code' => strtoupper(Str::random(10)),
                'voucher_id' => $voucher?->id,
                'payment_method' => $request->payment_method,
                'payment_status' => $request->payment_method === 'COD' ? 'Ch·ªù thanh to√°n' : 'Thanh to√°n th√†nh c√¥ng',
                'shipping_fee' => $shippingFee,
                'discount_amount' => $discount,
            ]);

            // Ghi nh·∫≠n vi·ªác s·ª≠ d·ª•ng voucher n·∫øu c√≥
            if ($voucher && $user) {
                VoucherUser::create([
                    'user_id' => $user->id,
                    'voucher_id' => $voucher->id,
                    'used_at' => Carbon::now(),
                ]);

                $voucher->increment('used_count');
                if ($voucher->used_count >= $voucher->usage_limit) {
                    $voucher->update(['status' => 'H·∫øt h·∫°n']);
                }
            }

            // T·∫°o b·∫£n ghi thanh to√°n
            $paymentStatus = $request->payment_method === 'VNPay'
                ? 'Thanh to√°n th√†nh c√¥ng'
                : 'Ch·ªù thanh to√°n';

            Payment::create([
                'order_id' => $order->id,
                'payment_method' => $request->payment_method,
                'payment_status' => $paymentStatus,
                'amount' => $finalTotal,
                'payment_date' => now(),
            ]);

            // N·∫øu l√† VNPay th√¨ c·∫≠p nh·∫≠t tr·∫°ng th√°i trong ƒë∆°n h√†ng (n·∫øu mu·ªën l∆∞u)
            if ($request->payment_method === 'VNPay') {
                $order->update(['payment_status' => 'Thanh to√°n th√†nh c√¥ng']);
            }

            foreach ($request->order_items as $item) {
                $variant = ProductVariant::find($item['product_variant_id']);
                if (!$variant) continue;

                OrderItem::create([
                    'order_id' => $order->id,
                    'product_variant_id' => $item['product_variant_id'],
                    'quantity' => $item['quantity'],
                    'total_price' => $variant->price * $item['quantity'],
                ]);

                $variant->decrement('stock', $item['quantity']);
            }

            return response()->json([
                'status' => true,
                'message' => 'ƒê∆°n h√†ng ƒë√£ ƒë∆∞·ª£c t·∫°o th√†nh c√¥ng',
                'order_id' => $order->id,
                'payment_method' => $order->payment_method,
            ], 201);
        } catch (ValidationException $e) {
            return response()->json([
                'status' => false,
                'message' => 'D·ªØ li·ªáu kh√¥ng h·ª£p l·ªá',
                'errors' => $e->errors(),
            ], 422);
        } catch (\Exception $e) {
            Log::error('L·ªói t·∫°o ƒë∆°n h√†ng: ' . $e->getMessage(), [
                'trace' => $e->getTraceAsString(),
                'request_data' => $request->all(),
            ]);
            return response()->json([
                'status' => false,
                'message' => 'L·ªói server: ' . $e->getMessage(),
            ], 500);
        }
    }

    public function update(Request $request, $id)
    {
        $order = Order::findOrFail($id);
