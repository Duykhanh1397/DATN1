                ->paginate(10); // ‚úÖ Ph√¢n trang chu·∫©n
        } else {
            $orders = Order::with(['orderItems.productVariant'])
                ->where('user_id', $user->id)
                ->orderBy('created_at', 'desc')
                ->paginate(10);
        }

        return response()->json([
            'status' => true,
            'message' => 'Danh s√°ch ƒë∆°n h√†ng',
            'data' => $orders
