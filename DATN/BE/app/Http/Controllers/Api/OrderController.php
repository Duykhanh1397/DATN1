        // $order = Order::with(['orderItems.productVariant', 'voucher', 'user'])->find($orderId);
        $order = Order::with([
            'user',
            'orderItems.productVariant.product',
            'orderItems.productVariant.color',
            'orderItems.productVariant.storage',
            'orderItems.productVariant.images'
        ])->findOrFail($orderId);


        if (!$order) {
            return response()->json(['status' => false, 'message' => 'ƒê∆°n h√†ng kh√¥ng t·ªìn t·∫°i'], 404);
        }

        // ‚úÖ Quy·ªÅn xem: Ch·ªâ ch·ªß ƒë∆°n ho·∫∑c Admin m·ªõi ƒë∆∞·ª£c xem
        if (Auth::user()->role !== 'Admin' && $order->user_id !== Auth::id()) {
            return response()->json(['status' => false, 'message' => 'B·∫°n kh√¥ng c√≥ quy·ªÅn xem ƒë∆°n h√†ng n√†y'], 403);
        }

        return response()->json([
            'status' => true,
            'message' => 'Chi ti·∫øt ƒë∆°n h√†ng',
            'data' => $order
        ]);
    }

    /**
